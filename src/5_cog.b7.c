void Cog_Update(){
	DMA_TRANSFER(38, &Cog_Tiles_Anim+CogOffset,0x9010); 
	TIMER++;
	CogOffset+=304;
	
	SSPEED++;;
	
	if (SSPEED == 2){SSPEED = 0;RPalOffset++;}
	if (CogOffset > 4800)CogOffset = 0;
	
	if (RPalOffset == 144)RPalOffset = 0;
	
	if ((TIMER > 12) && (TIMER < 48))WY_REG+=4;
	if ((TIMER > 730) && (TIMER < 766))WY_REG-=4;
	
	if (TIMER == 768){TIMER = 0; Scene++;}
}

void Cog_Set(){
    TIMER = 0; 
	
	VBK_REG = 0;	   
		set_bkg_tiles( 0, 0, 20, 18, Cog_MapPLN0); //tiles
		set_bkg_data( 0, 50, Cog_Tiles); //tile bank 1
	VBK_REG = 1;	   
		set_bkg_tiles( 0, 0, 20, 18, Cog_MapPLN1); //tiles
	VBK_REG = 0;	
	
	set_bkg_palette(0, 8, Cog_TilesPAL); //tile bank 1
	
	SHOW_BKG;
	HIDE_SPRITES;
	
	CogOffset = 0;
	RPalOffset = 0;
	SSPEED = 0;
	Scene++;
}	

void Cog_Run(){
	WAIT_SCANLINE1(); 
	RPal2 = LY_REG + RPalOffset;
	WAIT_SCANLINE2();
	Set_First_Palette_Col2(&CogDegrade+RPal2);
}