#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <ctype.h>


//---------------------------------------------------------------------------------
int main(int argc, char **argv) {
//---------------------------------------------------------------------------------
	FILE *fout;
	int size = sizeof(MapPLN1);
	int linelen = 0;
	
	fout = fopen("Map_Palette_Priority.c", "w");
	
	fprintf(fout,"\nunsigned char MapPLN1[] = {\n\t");
	
	for(int i = 0; i < size; i++) {
		unsigned char c = MapPLN1[i] | MapPLN2[i];
		fprintf(fout,"0x%02X", c);
		/* don't put a comma after the last item */
		if(i < (size-1)) fprintf(fout,",");
		linelen++;
		if (linelen == 16) {linelen = 0; fprintf(fout,"\n\t");}
	}
	if (linelen == 0) fprintf(fout,"\n};\n");

	fclose(fout);
	
	return 0;
}

